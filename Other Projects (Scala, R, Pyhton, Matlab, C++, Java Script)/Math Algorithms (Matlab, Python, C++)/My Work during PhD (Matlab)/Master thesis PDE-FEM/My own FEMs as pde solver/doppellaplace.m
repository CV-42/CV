function z = doppellaplace(x,y)
    if sqrt(x^2+y^2) >= 1/2
        z = 0;
    else
        z= (512*exp(1/(4*x^2 + 4*y^2 - 1))*(2048*x^10 + 10240*x^8*y^2 + 2304*x^8 + 20480*x^6*y^4 + 9216*x^6*y^2 - 1024*x^6 + 20480*x^4*y^6 + 13824*x^4*y^4 - 3072*x^4*y^2 + 40*x^4 + 10240*x^2*y^8 + 9216*x^2*y^6 - 3072*x^2*y^4 + 80*x^2*y^2 + 16*x^2 + 2048*y^10 + 2304*y^8 - 1024*y^6 + 40*y^4 + 16*y^2 - 1))/(4*x^2 + 4*y^2 - 1)^8;
    end
end